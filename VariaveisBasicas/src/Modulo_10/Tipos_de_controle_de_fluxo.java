/*
 * Aqui estão alguns dos principais tipos de controle de fluxo em Java:

Estruturas Condicionais:

if:

java
Copy code
if (condicao) {
    // código a ser executado se a condição for verdadeira
}
if-else:

java
Copy code
if (condicao) {
    // código a ser executado se a condição for verdadeira
} else {
    // código a ser executado se a condição for falsa
}
else-if:

java
Copy code
if (condicao1) {
    // código a ser executado se a condição1 for verdadeira
} else if (condicao2) {
    // código a ser executado se a condição2 for verdadeira
} else {
    // código a ser executado se nenhuma das condições anteriores for verdadeira
}
Estruturas de Repetição:

for:

java
Copy code
for (int i = 0; i < 10; i++) {
    // código a ser repetido enquanto a condição for verdadeira
}
while:

java
Copy code
while (condicao) {
    // código a ser repetido enquanto a condição for verdadeira
}
do-while:

java
Copy code
do {
    // código a ser repetido pelo menos uma vez e enquanto a condição for verdadeira
} while (condicao);
Switch-Case:

java
Copy code
switch (expressao) {
    case valor1:
        // código a ser executado se expressao for igual a valor1
        break;
    case valor2:
        // código a ser executado se expressao for igual a valor2
        break;
    // ...
    default:
        // código a ser executado se nenhum dos casos anteriores for correspondido
}
Controle de Fluxo Específico:

break:
Utilizado para sair de loops ou do bloco de switch.

java
Copy code
for (int i = 0; i < 10; i++) {
    if (i == 5) {
        break; // sai do loop quando i for igual a 5
    }
    // código a ser executado dentro do loop
}
continue:
Utilizado para pular a iteração atual de um loop e continuar com a próxima.

java
Copy code
for (int i = 0; i < 10; i++) {
    if (i == 5) {
        continue; // pula a iteração quando i for igual a 5
    }
    // código a ser executado dentro do loop (exceto quando i for igual a 5)
}
*/

