package Modulo_8;

public class Tipo_de_Variavel_Basica {

	public static void main(String[] args) {
		
		/**
		 * Tipos de Variáveis
		 * 
		 * Variáveis Primitivas: Estas são variáveis que armazenam valores simples, não são objetos. Existem oito tipos primitivos no Java:

			byte: 8 bits, armazena valores inteiros.
			short: 16 bits, armazena valores inteiros.
			int: 32 bits, armazena valores inteiros.
			long: 64 bits, armazena valores inteiros longos.
			float: 32 bits, armazena valores de ponto flutuante.
			double: 64 bits, armazena valores de ponto flutuante.
			char: 16 bits, armazena caracteres Unicode.
			boolean: armazena valores true ou false.
			Variáveis de Referência: Estas variáveis referenciam objetos em memória. Os tipos de variáveis de referência incluem:
			
			Classes (tipos de classe que você cria)
			Interfaces (tipos abstratos que definem contratos)
			Arrays (matrizes de objetos ou primitivos)
			Variáveis Locais: Variáveis declaradas em um método ou bloco e só existem durante a execução desse método ou bloco.
			
			Variáveis de Instância: Variáveis pertencentes a uma instância de uma classe e são inicializadas quando um objeto da classe é criado.
			
			Variáveis de Classe (ou estáticas): Variáveis associadas à classe em vez de uma instância específica e compartilhadas por todas as instâncias dessa classe.
			
			Variáveis Final: São constantes que não podem ser modificadas após a sua atribuição.
			
			Variáveis Voláteis: Usadas em programação concorrente para indicar que uma variável pode ser modificada por várias threads.
					 
		 */
		
	}
}
